    <h2>MY METHODS</h2>
    <p class="mTitle">Selling your home and wondering which selling method will suit you best?</p>
    <p>There are many different options available to you such as a Fixed Price, Auction, Tender, Deadline Sale and By Negotiation.</p>
    <p>Each of these selling methods has its pros and cons, so it’s just a case of discussing them with your Agent to decide which suits you and your situation best.</p>
    <div class="accordian mobile">
      <h3>Fixed Price</h3>
      <div>
        <p>
          <strong>A Fixed price is where the property is offered at a price determined by you, the seller.</strong> When a buyer expresses interest in the property, a Metro Consultant negotiates on your behalf with the buyer in order to maximize the best price and outcome for our Vendors.</p>
        <p>Many Purchasers, like the fact that a price is defined, and would therefore prefer to buy through this method. The seller of the property must commit to a price when the property goes onto the market; therefore it is critical that you set the price correctly.</p>
      </div>
      <h3>For Sale by Negotiation</h3>
      <div>
        <p>
          <strong>This method of sale offers your property to the market with no price.</strong> The buyers with an interest in your property are encouraged to make an offer and negotiate. When a buyer expresses interest in the property, a Metro Consultant negotiates with the buyer to try and achieve the best possible results.</p>
      </div>
      <h3>Auction</h3>
      <div>
        <p>
          <strong>The Auction method creates a situation where buyers have to compete with other buyers, rather than competing with you, the Vendor.</strong> When auctioning your property it cannot be sold unless it reaches your ‘reserve price’ - however if there is competitive bidding it can be sold for well above the reserve price. Because there is no asking price, the buyers fall in love with the property first and don’t have the issue of price as a potential objection.</p>
        <p>Auction means that prospective buyers will bid against one another at a date and time set by you. Both the seller and buyers can see how much interest there is in the particular property and, importantly, each buyer can see who they are bidding against.</p>
        <p>Other auction benefits to the Vendor are:</p>
        <p>It significantly raises the profile of your home through a carefully targeted, intensive advertising campaign. It places a time limit on buyers, encouraging them to make a high offer before the auction rather than wait to see what auction day may bring, allowing you the option of selling before auction if you choose.</p>
        <p>It allows the seller to set the terms and conditions of the sale, it creates competition between buyers, pushing the price up rather than negotiating it down. It is an unconditional sale upon the fall of the hammer and highest bidder is the Owner.</p>
      </div>
      <h3>Tender</h3>
      <div>
        <p>
          <strong>Selling by tender is a method of exclusively marketing your home without a listed price.</strong> Written offers, called tenders, are accepted up to a certain time and date set by you. Once the tenders are opened and examined, the highest of any tender is not necessarily accepted. The seller can negotiate with any tenderer.</p>
        <p>The purchase price and conditions of any tenders are not disclosed publicly or to any other tenderer. Once the Tender process has begun no offers may be accepted prior to the date set for the Tender.</p>
      </div>
      <h3>Deadline Sale</h3>
      <div>
        <p>
          <strong>The Deadline sale process is again a method of marketing your home without a listed price.</strong> Similar to a Tender, the Deadline Sale has written offers to be submitted by a certain date and time set by you.</p>
        <p>These offers are made on a standard “Sale and Purchase Agreement” (rather than specific documents being made as in a tender situation). The Vendor also has the right to negotiate with any offer and can sell their property prior to the nominated date if they wish.</p>
      </div>
